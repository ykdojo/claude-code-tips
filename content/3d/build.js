#!/usr/bin/env node
// Scans HTML for <!-- 3D: model canvas-id slide-id --> markers,
// generates script blocks, and injects them between
// <!-- 3D-SCRIPTS-START --> and <!-- 3D-SCRIPTS-END -->.

const fs = require('fs');
const path = require('path');

const htmlFile = process.argv[2];
if (!htmlFile) {
  console.error('Usage: node build.js <html-file>');
  process.exit(1);
}

const html = fs.readFileSync(htmlFile, 'utf-8');
const dir = __dirname;

// Find all <!-- 3D: model canvas-id slide-id --> markers
const markerRegex = /<!-- 3D: (\S+) (\S+) (\S+) -->/g;
const markers = [];
let match;
while ((match = markerRegex.exec(html)) !== null) {
  markers.push({ model: match[1], canvasId: match[2], slideId: match[3] });
}

if (markers.length === 0) {
  console.log('No 3D markers found.');
  process.exit(0);
}

// Generate script blocks
const baseTemplate = fs.readFileSync(path.join(dir, 'base.js'), 'utf-8');
let scripts = '';

for (const { model, canvasId, slideId } of markers) {
  const modelFile = path.join(dir, `${model}.js`);
  if (!fs.existsSync(modelFile)) {
    console.error(`Error: ${modelFile} not found`);
    process.exit(1);
  }
  const modelCode = fs.readFileSync(modelFile, 'utf-8');
  const baseCode = baseTemplate
    .replace(/__CANVAS_ID__/g, canvasId)
    .replace(/__SLIDE_ID__/g, slideId);

  scripts += `\n  <script>\n    // 3D ${model} scene (generated by 3d/build.js)\n${modelCode}\n${baseCode}  </script>\n`;
}

// Replace between markers
const startMarker = '<!-- 3D-SCRIPTS-START -->';
const endMarker = '<!-- 3D-SCRIPTS-END -->';
const startIdx = html.indexOf(startMarker);
const endIdx = html.indexOf(endMarker);

if (startIdx === -1 || endIdx === -1) {
  console.error('Missing <!-- 3D-SCRIPTS-START --> or <!-- 3D-SCRIPTS-END --> markers in HTML');
  process.exit(1);
}

const newHtml = html.substring(0, startIdx + startMarker.length) + scripts + '  ' + html.substring(endIdx);
fs.writeFileSync(htmlFile, newHtml);
console.log(`Updated ${markers.length} 3D slide(s) in ${htmlFile}`);
